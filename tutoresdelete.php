			if ($sMasterTblVar <> "personas") {
				if ($this->Dni_Tutor->CurrentValue == "") $this->Dni_Tutor->setSessionValue("");
			}
		}
		$this->DbMasterFilter = $this->GetMasterFilter(); // Get master filter
